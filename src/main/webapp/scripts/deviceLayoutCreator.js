function createDeviceLayout(layoutArr) {
	var len = layoutArr.length;
	var $layoutCtn = $('<div class="layout-ctn clearfix"></div>');
	$.each(layoutArr, function(i, layoutItem) {
		var $layoutItem = $('<div class="layout-item clearfix" style="width:' + (1/len*100+'%') + ';"></div>');
		$layoutItem.appendTo($layoutCtn);

		$.each(layoutItem, function(i, aisleOpt) {
			var $aisle = $('<div class="aisle-wrapper" style="width:' + (aisleOpt.width*100+'%') + ';"><div class="aisle-block" aisle="' + aisleOpt.aisle + '"></div></div>');
			$aisle.appendTo($layoutItem);
		})
	})

	return $layoutCtn;
}